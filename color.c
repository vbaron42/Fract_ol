int			color_scale(int i)
{
	while (i >= 768)
		i -= 768;
	if (i < 256)
		return (0x0000FF + (i * 0x010000) - i);
	else if (i < 512)
		return (0xFF0000 - ((i - 256) * 0x010000) + ((i - 256) * 0x000100));
	else
		return (0x00FF00 - ((i - 512) * 0x000100) + (i - 512));
}
